    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
           Statement stmt;
        String precio = this.jTextField4.getText();
        float pre= Float.valueOf(precio);
        String porcentaje = this.jTextField3.getText();
        float porcen = Float.valueOf(porcentaje);
        float total =((pre*porcen)/100);
        float res = total + pre;

        try {
            
            stmt = conexion.conexion.createStatement();
            String sql = "UPDATE producto SET nombre='" + this.jTextField1.getText() +
                         "', cantidad='" + this.jTextField2.getText() + 
                         "', porcentaje='" + this.jTextField3.getText() + 
                         "', preciocosto='" + this.jTextField4.getText() +
                         "', precioventa='" + res +
                         "' WHERE id='"+ this.jComboBox1.getItemAt(jComboBox1.getSelectedIndex()).getId()+ "'; ";
            stmt.execute(sql); 
            
        }catch (SQLException f) {
            JOptionPane.showMessageDialog(null, "Error 1");
            return;
        }catch (Exception f) {
            JOptionPane.showMessageDialog(null, "Error 2");
            return;
        }
         
        JOptionPane.showMessageDialog(null, "Registro actualizado...");
    }//GEN-LAST:event_jButton1ActionPerformed

